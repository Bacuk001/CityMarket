/**
 * Class storing basic information about the store.
 */
export class Market {
  /**
   * Store identification number.
   */
  public id: number;
  /**
   * Market name.
   */
  public name: string;
  /**
   * Market address.
   */
  public addres: string;
  /**
   * Market description.
   */
  public about: string;
}
